export const formatAge = (months) => {
  if (!months) return 'Unknown';
  if (months < 12) return `${months} months`;
  const years = Math.floor(months / 12);
  const rem = months % 12;
  return rem > 0 ? `${years}y ${rem}m` : `${years} ${years === 1 ? 'year' : 'years'}`;
};

export const formatDuration = (d, lang = 'en') => {
  const map = {
    en: { FEW_DAYS: 'Few days', ONE_TWO_WEEKS: '1-2 weeks', ONE_MONTH: '1 month', UNTIL_ADOPTION: 'Until adoption', UNKNOWN: 'Unknown' },
    ro: { FEW_DAYS: 'Cateva zile', ONE_TWO_WEEKS: '1-2 saptamani', ONE_MONTH: '1 luna', UNTIL_ADOPTION: 'Pana la adoptie', UNKNOWN: 'Nu se stie' },
  };
  return (map[lang] || map.en)[d] || d;
};

export const formatUrgency = (u, lang = 'en') => {
  const map = {
    en: { LOW: 'Low', MEDIUM: 'Medium', HIGH: 'Urgent' },
    ro: { LOW: 'Scazut', MEDIUM: 'Mediu', HIGH: 'Urgent' },
  };
  return (map[lang] || map.en)[u] || u;
};
